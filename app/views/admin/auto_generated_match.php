<script>
(function(){
    const searchInput = document.getElementById('agmSearch');
    const rows = () => Array.from(document.querySelectorAll('.agm-row'));
    const showSel = document.getElementById('agmShowEntries');
    const infoEl = document.getElementById('agmInfo');
    const pagEl = document.getElementById('agmPagination');
    let currentPage = 1;
    let activeMatchTab = 'all';

    function filtered() {
        const q = (searchInput && searchInput.value ? searchInput.value : '').toLowerCase().trim();
        return rows().filter(function(r) {
            if (activeMatchTab !== 'all') {
                const st = (r.getAttribute('data-match-status') || '').trim();
                if (st !== activeMatchTab) return false;
            }
            return !q || (r.getAttribute('data-search') || '').indexOf(q) !== -1;
        });
    }

    document.querySelectorAll('.agm-tab').forEach(function(btn) {
        btn.addEventListener('click', function() {
            activeMatchTab = (btn.getAttribute('data-agm-tab') || 'all');
            document.querySelectorAll('.agm-tab').forEach(function(b) { b.classList.remove('active'); });
            btn.classList.add('active');
            currentPage = 1;
            render();
        });
    });

    function appendPageLink(text, targetPage, isActive, isDisabled) {
        const li = document.createElement('li');
        li.className = 'page-item';
        if (isActive) li.classList.add('active');
        if (isDisabled) li.classList.add('disabled');
        const isStatic = isActive || isDisabled;
        const el = document.createElement(isStatic ? 'span' : 'a');
        el.className = 'page-link';
        el.textContent = text;
        if (!isStatic) {
            el.href = '#';
            el.addEventListener('click', function(e) {
                e.preventDefault();
                currentPage = targetPage;
                render();
            });
        }
        li.appendChild(el);
        pagEl.appendChild(li);
    }

    function render() {
        const all = filtered();
        const total = all.length;
        let per = parseInt(showSel && showSel.value ? showSel.value : '10', 10);
        if (isNaN(per) || per < 1) per = 10;
        let pages = 0;
        if (total === 0) { pages = 0; currentPage = 1; }
        else if (per >= 9999) { pages = 1; }
        else { pages = Math.max(1, Math.ceil(total / per)); }
        if (currentPage > pages && pages > 0) currentPage = pages;
        if (currentPage < 1) currentPage = 1;
        const start = total === 0 ? 0 : (per >= 9999 ? 1 : (currentPage - 1) * per + 1);
        const end = total === 0 ? 0 : (per >= 9999 ? total : Math.min(currentPage * per, total));

        rows().forEach(function(r) { r.style.display = 'none'; });
        if (per >= 9999) {
            all.forEach(function(r) { r.style.display = ''; });
        } else {
            all.slice((currentPage - 1) * per, currentPage * per).forEach(function(r) { r.style.display = ''; });
        }

        infoEl.textContent = total === 0 ? 'Showing 0 to 0 of 0 entries' : ('Showing ' + start + ' to ' + end + ' of ' + total + ' entries');

        pagEl.innerHTML = '';
        if (pages <= 0) return;

        appendPageLink('Previous', currentPage - 1, false, currentPage === 1);
        for (let p = 1; p <= pages; p++) {
            appendPageLink(String(p), p, p === currentPage, false);
        }
        appendPageLink('Next', currentPage + 1, false, currentPage === pages);
        appendPageLink('Last', pages, false, currentPage === pages);
    }

    searchInput && searchInput.addEventListener('keyup', function() { currentPage = 1; render(); });
    document.getElementById('agmSearchBtn') && document.getElementById('agmSearchBtn').addEventListener('click', function() { currentPage = 1; render(); });
    document.getElementById('agmClearSearch') && document.getElementById('agmClearSearch').addEventListener('click', function() {
        searchInput.value = ''; currentPage = 1; render();
    });
    showSel && showSel.addEventListener('change', function() { currentPage = 1; render(); });
    render();
})();
</script>

