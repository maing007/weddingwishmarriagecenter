$agmStatusBadgeClass = static function (string $s): string {
    $s = trim($s);
    $map = [
        'Pending' => 'warning',
        'Notified' => 'info',
        'Declined' => 'danger',
        'Accepted' => 'success',
    ];

    return $map[$s] ?? 'secondary';
};
$fmtDt = static function ($raw): string {
    if ($raw === null || $raw === '') {
        return 'N/A';
    }
    $t = strtotime((string) $raw);

    return $t ? date('M j, Y g:i A', $t) : 'N/A';
};
$na = static function ($v) {
    if ($v === null || $v === '') {
        return 'N/A';
    }

    return (string) $v;
};

